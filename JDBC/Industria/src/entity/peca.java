package entity;

public class peca {

	private int numPeca;
	private double pesoPeca;
	private String corPeca;
	private int numDeposito;
	
	public int getNumPeca() {
		return numPeca;
	}
	public void setNumPeca(int numPeca) {
		this.numPeca = numPeca;
	}
	public double getPesoPeca() {
		return pesoPeca;
	}
	public void setPesoPeca(double pesoPeca) {
		this.pesoPeca = pesoPeca;
	}
	public String getCorPeca() {
		return corPeca;
	}
	public void setCorPeca(String corPeca) {
		this.corPeca = corPeca;
	}
	public int getNumDeposito() {
		return numDeposito;
	}
	public void setNumDeposito(int numDeposito) {
		this.numDeposito = numDeposito;
	}
}
