package entity;

public class endereco {

	private int codEndereco;
	private String lograEndereco;
	private int numEndereco;
	private String cidadeEndereco;
	private String estadoEndereco;
	private Integer numFornecedor;
	private Integer numDeposito;
	
	public int getCodEndereco() {
		return codEndereco;
	}
	public void setCodEndereco(int codEndereco) {
		this.codEndereco = codEndereco;
	}
	public String getLograEndereco() {
		return lograEndereco;
	}
	public void setLograEndereco(String lograEndereco) {
		this.lograEndereco = lograEndereco;
	}
	public int getNumEndereco() {
		return numEndereco;
	}
	public void setNumEndereco(int numEndereco) {
		this.numEndereco = numEndereco;
	}
	public String getCidadeEndereco() {
		return cidadeEndereco;
	}
	public void setCidadeEndereco(String cidadeEndereco) {
		this.cidadeEndereco = cidadeEndereco;
	}
	public String getEstadoEndereco() {
		return estadoEndereco;
	}
	public void setEstadoEndereco(String estadoEndereco) {
		this.estadoEndereco = estadoEndereco;
	}
	public Integer getNumFornecedor() {
		return numFornecedor;
	}
	public void setNumFornecedor(Integer numFornecedor) {
		this.numFornecedor = numFornecedor;
	}
	public Integer getNumDeposito() {
		return numDeposito;
	}
	public void setNumDeposito(Integer numDeposito) {
		this.numDeposito = numDeposito;
	}
	
	
}
