package entity;

public class projeto {

	private int numProjeto;
	private double orcaProjeto;
	
	public int getNumProjeto() {
		return numProjeto;
	}
	public void setNumProjeto(int numProjeto) {
		this.numProjeto = numProjeto;
	}
	public double getOrcaProjeto() {
		return orcaProjeto;
	}
	public void setOrcaProjeto(double orcaProjeto) {
		this.orcaProjeto = orcaProjeto;
	}
	
}
