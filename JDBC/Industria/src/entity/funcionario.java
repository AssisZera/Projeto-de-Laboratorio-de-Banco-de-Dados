package entity;

public class funcionario {

	private int numFunc;
	private String nomeFunc;
	private double salariFunc;
	private int numDepart;
	private String dataInicioFunc;
	
	public int getNumFunc() {
		return numFunc;
	}
	public void setNumFunc(int numFunc) {
		this.numFunc = numFunc;
	}
	public String getNomeFunc() {
		return nomeFunc;
	}
	public void setNomeFunc(String nomeFunc) {
		this.nomeFunc = nomeFunc;
	}
	public double getSalariFunc() {
		return salariFunc;
	}
	public void setSalariFunc(double salariFunc) {
		this.salariFunc = salariFunc;
	}
	public int getNumDepart() {
		return numDepart;
	}
	public void setNumDepart(int numDepart) {
		this.numDepart = numDepart;
	}
	public String getDataInicioFunc() {
		return dataInicioFunc;
	}
	public void setDataInicioFunc(String dataInicioFunc) {
		this.dataInicioFunc = dataInicioFunc;
	}
	
}
