package entity;

public class deposito {

	private int numDeposito;
	private String nomeDeposito;
	
	public int getNumDeposito() {
		return numDeposito;
	}
	public void setNumDeposito(int numDeposito) {
		this.numDeposito = numDeposito;
	}
	public String getNomeDeposito() {
		return nomeDeposito;
	}
	public void setNomeDeposito(String nomeDeposito) {
		this.nomeDeposito = nomeDeposito;
	}
}
